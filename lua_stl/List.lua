--[[
目标是解决:
	频繁插入/删除时, list 的性能问题(只有在频繁增删的情况下有优势)

	不能批量插入/删除
--]]



local List = {}

List.__index = List



--创建链表(自己保证元素的一致性, 否则没办法排序)
function List:New( Type )
	local t = {}
	local Setmetatable = setmetatable
	if Type ~= "number" and Type ~= "string" and Type ~= "anything" then
		return nil
	end
	Setmetatable(t, self)
	t.__index = self
	t.Type = Type
	t.Len = 0
	t.Prev = nil
	t.Next = nil
	return t
end

--返回第一个元素
--成功返回节点元素值, 失败返回nil
function List:Front()
	local firstNode = self.Next
	if nil ~= firstNode then
		return firstNode.val
	else
		return nil
	end
end

--返回最后一个元素
--成功返回节点的val元素值, 失败返回nil
function List:Back()
	local lastNode = self.Prev
	if nil ~= lastNode then
		return lastNode.val
	else
		return nil
	end
end

--从List头部插入元素
function List:PushFront( val )
	local Len = self.Len
	local fristNode = self.Next
	--创建节点
	local node = { val = val, prev = nil, next = nil }
	--node.prev = nil
	if Len <= 0 then
		self.Prev = node
		--self.Next = node
	else
		--节点互扣
		node.next = fristNode
		fristNode.prev = node
		--self.Next = node
	end
	self.Next = node
	self.Len = Len + 1
end

--从List尾部插入元素
function List:PushBack( val )
	local Len = self.Len
	local lastNode = self.Prev
	--创建节点
	local node = { val = val, prev = nil, next = nil }
	--node.next = nil
	if Len <= 0 then
		self.Next = node
		--self.Prev = node
	else
		--节点互扣
		node.prev = lastNode
		lastNode.next = node
		--self.Prev = node
	end
	self.Prev = node
	self.Len = Len + 1
end

--从List头部弹出元素
--成功返回节点的val元素值, 失败返回nil
function List:PopFront()
	local Len = self.Len
	local fristNode = self.Next
	local val = nil
	--fristNode.prev = nil
	if Len <= 0 then
		return nil
	else
		val = fristNode.val
		if Len == 1 then
			--List已空, 复原
			self.Next = nil
			self.Prev = nil
			self.Len = 0
		else
			self.Next = fristNode.next
			self.Len = Len - 1
		end
		--强制释放字节资源?
		fristNode.val = nil
		fristNode.prev = nil
		fristNode.next = nil
		return val
	end
end

--从List尾部弹出元素
--成功返回节点的val元素值, 失败返回nil
function List:PopBack()
	local Len = self.Len
	local lastNode = self.Prev
	local val = nil
	--lastNode.next = nil
	if Len <= 0 then
		return nil
	else
		val = lastNode.val
		if Len == 1 then
			--List已空, 复原
			self.Next = nil
			self.Prev = nil
			self.Len = 0
		else
			self.Prev = lastNode.prev
			self.Len = Len - 1
		end
		--强制释放字节资源?
		lastNode.val = nil
		lastNode.prev = nil
		lastNode.next = nil
		return val
	end
end

--根据val元素值, 查询元素是否存在, 存在则返回'第一个找到的元素'的node节点
--成功返回目标节点node, 失败返回nil
function List:Find( val )
	local Len = self.Len
	local i = 1
	local fristNode = self.Next
	for i=1,Len,1 do
		if fristNode.val == val then
			return fristNode
		else
			fristNode = fristNode.next
		end
	end
	return nil
end

--队列空?
--成功返回true, 失败返回false
function List:Empty()
	local Len = self.Len
	return ((Len <= 0)  and  true  or  false)
end

--返回List长度
function List:Count()
	local Len = self.Len
	return Len
end

--清空
function List:Clear()
	local Len = self.Len
	local i = 1
	local fristNode = self.Next
	local tmp = nil
	local Collectgarbage = collectgarbage
	if Len >= 2 then
		for i=1,Len-1,1 do
			--全部清洗
			tmp = fristNode.next
			fristNode.val = nil
			fristNode.prev = nil
			fristNode.next = nil
			fristNode = tmp
		end
		--强制执行一次垃圾回收, 清空缓存
		Collectgarbage("collect")
	else
		fristNode.val = nil
		fristNode.prev = nil
		fristNode.next = nil
	end
	self.Len = 0
	self.Prev = nil
	self.Next = nil
end

--根据val 值, 查找与该val 值匹配的'第一个找到的元素', 然后删除
function List:Remove( val )
	local Len = self.Len
	local node = self:Find(val)
	local PrevNode, NextNode = nil, nil
	if node ~= nil then
		--指针初赋值
		PrevNode = node.prev
		NextNode = node.next
		--执行删除
		PrevNode.next = NextNode
		NextNode.prev = PrevNode
		self.Len = Len - 1
		--全部清洗
		node.val = nil
		node.prev = nil
		node.next = nil
	end
end

--根据val 值, 查找与该val 值匹配的'第一个找到的元素', 然后在其后一位插入元素
function List:Insert( index_val, val )
	local node_new = { val = val, prev = nil, next = nil }
	local Len = self.Len
	local node = self:Find(index_val)
	local PrevNode, NextNode = nil, nil
	if node ~= nil then
		--指针初赋值
		PrevNode = node.prev
		NextNode = node.next
		--执行插入
		PrevNode.next = node_new
		NextNode.prev = node_new
		node_new.next = NextNode
		node_new.prev = PrevNode
		self.Len = Len + 1
	end
end

--对List 进行排序(拆散, 将元素倒入新的table, sort后再倒出)
--直接交换两个节点的元素值即可, 不需要动链表结构!!
--成功返回一个'带有所有元素的有序table', 失败返回nil
--(该函数可以两用: 可以先排序, 然后返回一个'带有所有元素的有序table';
-- 也可以仅排序, 不要table;
-- List转换为table的性能消耗, 和排序是一样的, 可以两用, 尽量两用)
function List:SortEx()
	local Type = self.Type
	local Len = self.Len
	local fristNode = self.Next
	local Sort = table.sort
	local i = 1
	local t = {}
	if Type == "anything" or Len <= 1 then
		--非元素统一的List 拒绝排序, 只有一个元素, 拒绝排序
		return nil
	else
		--number or string都行,拆散&并入table后,直接table.sort()进行排序,省事省力,性能也可以
		for i=1,Len,1 do
			t[i] = fristNode.val
			fristNode = fristNode.next
		end
		--进行排序
		Sort(t)
		--复原fristNode 指针位置
		fristNode = self.Next
		--将排序后的数据, 交换到List中
		for i=1,Len,1 do
			fristNode.val = t[i]
			fristNode = fristNode.next
		end
		return t
	end
end

