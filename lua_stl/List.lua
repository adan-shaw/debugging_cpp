--对List 进行'洗牌乱序'
--成功返回一个带有所有元素的无序table, 失败返回nil
function List:ShuffleEx()
	local Type = self.Type
	local Len = self.Len
	local fristNode = self.Next
	local Random = math.random
	local i, count = 1, Len
	local r1, r2 = 0, 0
	local tmp = nil
	local t = {}
	if Type == "anything" or Len <= 1 then
		--非元素统一的List 拒绝排序, 只有一个元素, 拒绝乱序
		return nil
	else
		--number or string都行,拆散&并入table后,直接table.sort()进行排序,省事省力,性能也可以
		for i=1,Len,1 do
			t[i] = fristNode.val
			fristNode = fristNode.next
		end
		--进行乱序
		for i=1,count,1 do
			r1 = Random(1, Len)
			r2 = Random(1, Len)
			tmp = t[r1]
			t[r1] = t[r2]
			t[r2] = tmp
		end
		--复原fristNode 指针位置
		fristNode = self.Next
		--将乱序后的数据, 交换到List中
		for i=1,Len,1 do
			fristNode.val = t[i]
			fristNode = fristNode.next
		end
		return t
	end
end

